#include <xc.h>

#define _XTAL_FREQ 4000000  // 4 MHz Clock Frequency

// Common Anode 7-Segment Display Encoding (0-9)
const unsigned char segment_digits[10] = {
    0xC0, // 0
    0xF9, // 1
    0xA4, // 2
    0xB0, // 3
    0x99, // 4
    0x92, // 5
    0x82, // 6
    0xF8, // 7
    0x80, // 8
    0x90  // 9
};

// Function to display a number on all 4 digits synchronously
void display_number(unsigned char num) {
    LATD = segment_digits[num];  // Invert the segment pattern for Common Anode

    // Activate all digits simultaneously
    LATC = 0x0F;  // Set all control lines HIGH to turn ON transistors

    __delay_ms(500);  // Hold the number for visibility
}

void main() {
    TRISD = 0x00;  // Set PORTD as output (for 7-segment segments)
    TRISC = 0x00;  // Set PORTC as output (for digit control)

    LATD = 0xFF;   // Set all segments OFF initially (Common Anode requires HIGH to turn OFF)
    LATC = 0x00;   // All digits initially OFF

    while (1) {
        for (unsigned char i = 0; i < 10; i++) {
            display_number(i);  // Display numbers 0-9
        }
    }
}
